
public class Constructors {

	public static void main(String[] args) {

		long l = 1000; // primitive type
		
		Long a = new Long(l); // Long a is an object. Converting to an object from a primitive type
		
		String s = "2000"; // Primitive Type String
		
		Long b = new Long(s); // Long b is an object, converting a primitive string to an object
				
	}

}
